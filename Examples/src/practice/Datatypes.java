package practice;

public class Datatypes {
	
		//Java is simple programming language 
		//Object oriented programming language
		//platform independent
		public static void main(String[] args) {
			//Operators(Arithmetic,relational,conditional)
			
			//Primitive datatypes and Non Primitive datatypes
			//Primitive datatype -built in datatypes in java
			//stored in stack memory,fast and light weight
			//no built in methods.
			//default values 0,false.
		
			//Numbers,decimals,alphabets,specialcharacters
			//9858,95876.948, slfjtirn, &^*%$#@,
			//variable is nothing but a container to store the data
			
			byte b = 127; //8bit - fixed memory //variable initialize
			
			short s = b ; //16 bit-fm  //upcasting --small bit size to large bitsize
			
			int x = 49768 ;//32 bit //variable declaration //values assigning = initialization.
			
			
			float f = 49768; //implicit casting -- internal --49768.0f 
			System.out.println(f);
			
			int v=32767; //32 bit-fm
			
			long lg = 32767946968698l; //(implicit casting)-upcasting / widening casting 
			
			double d1 = 588.94d;//64 bit
			
			int itt = (int) lg;  //downcasting - narrowing casting-explicit casting
			System.out.println(itt);
			
			
			
			char a = 0 ;//default value of char
			boolean bn = true;
			boolean bb = false; //default value of boolean
			
			System.out.println(bb);
			
			
			float f1 = 948569.47f ; //32bit		
			
			double d = 0 ; //64bit
			long L = 0 ; //64 bit-fm
			System.out.println(L);
			
			
			//Non primitive datatypes--String, Array,Class,Interface
			//Stored in heap memory
			
			//slower than primitive datatype
			//have methods 
			//default value is null
			
			//typecasting -- Implicit casting(Upcasting), explicit casting(downcasting)
			
			
			
			//Autoboxing and unboxing -- 
			//--primitive datatypes into Wrapper classes object, wrapper classes objects into corresponding primitive datatype
			
			
			

		}

	}

	


